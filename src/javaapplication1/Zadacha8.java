package javaapplication1;

public class Zadacha8 {
     public static void myMetod(double k, double l){
        System.out.println(k+"=="+l+" " + (k==l));
        System.out.println(k+">"+l+" " + (k>l));
        System.out.println(k+"<"+l+" " + (k<l));
        System.out.println(k+">="+l+" " + (k>=l));
        System.out.println(k+"<="+l+" " + (k<=l));
        System.out.println(k+"!="+l+" " + (k!=l));
    }
}
